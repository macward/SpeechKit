// SpeechKit - Voice I/O Library for Apple Platforms
//
// A Swift package providing speech recognition (voice-to-text) and
// speech synthesis (text-to-voice) functionality using a provider-based
// architecture.
//
// ## Recognition
// - `SpeechRecognitionEngine` - Main entry point for voice recognition
// - `SpeechRecognitionProvider` - Protocol for recognition providers
// - `SFSpeechRecognizerProvider` - Apple's SFSpeechRecognizer implementation
// - `SpeechAnalyzerProvider` - iOS 26+ SpeechAnalyzer implementation
//
// ## Synthesis
// - `SpeechSynthesisEngine` - Main entry point for speech synthesis
// - `SpeechSynthesisProvider` - Protocol for synthesis providers
// - `AppleSpeechSynthesisProvider` - Apple's AVSpeechSynthesizer implementation

